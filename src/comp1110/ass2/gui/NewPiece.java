public class NewPiece {
    int[][][] p = new int [3][3][2];

    public NewPiece(char pieceName) {
        switch (pieceName) {
            case 'A':   p[0][0][0] = 1;
                p[1][1][0] = 1;
                p[0][2][0] = 1;
                p[0][1][1] = 1;
                p[1][2][1] = 1;
                p[2][1][1] = 1;
                break;
            case 'B':   p[1][1][0] = 1;
                p[2][0][0] = 1;
                p[1][0][1] = 1;
                p[1][2][1] = 1;
                p[2][1][1] = 1;
                break;

            case 'C':   p[1][1][0] = 1;
                p[0][0][0] = 1;
                p[1][0][1] = 1;
                p[1][2][1] = 1;
                p[2][1][1] = 1;
                break;

            case 'D':   p[1][1][0] = 1;
                p[2][0][0] = 1;
                p[1][0][1] = 1;
                p[1][2][1] = 1;
                p[0][1][1] = 1;
                break;

            case 'E':   p[1][1][0] = 1;
                p[0][0][0] = 1;
                p[1][0][1] = 1;
                p[1][2][1] = 1;
                p[0][1][1] = 1;
                break;

            case 'F':   p[0][0][0] = 1;
                p[1][1][0] = 1;
                p[2][2][0] = 1;
                p[2][1][1] = 1;
                p[1][0][1] = 1;
                break;

            case 'G':   p[0][0][0] = 1;
                p[1][1][0] = 1;
                p[2][2][0] = 1;
                p[2][1][1] = 1;
                p[1][0][1] = 1;
                p[1][2][1] = 1;
                break;

            case 'H':   p[2][0][0] = 1;
                p[1][1][0] = 1;
                p[2][2][0] = 1;
                p[0][1][1] = 1;
                p[1][0][1] = 1;
                p[1][2][1] = 1;
                break;

        }
    }


}
